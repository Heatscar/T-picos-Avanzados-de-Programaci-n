    public static boolean existePunto(String textoPantalla){
        boolean existePunto;
        existePunto = false;
        for(int i = 0; i < textoPantalla.length(); i++){
            if(textoPantalla.substring(i, i + 1).equals(".")){
                existePunto = true;
            }
        }
        
        return existePunto;
    }    
    
    public static String calculadora(String numero1, String numero2, String operacion){
        Double resultado = 0.0;
        String respuesta;
        
        if(operacion.equals("+")){
            resultado = (Double.parseDouble(numero1) + Double.parseDouble(numero2));
        }
        
        if(operacion.equals("-")){
            resultado = Double.parseDouble(numero1) - Double.parseDouble(numero2);
        }
        
        if(operacion.equals("*")){
            resultado = Double.parseDouble(numero1) * Double.parseDouble(numero2);
        }
        
        if(operacion.equals("/")){
            resultado = Double.parseDouble(numero1) / Double.parseDouble(numero2);
        }
        
        if(operacion.equals("%")){
            resultado = Double.parseDouble(numero1) * (Double.parseDouble(numero2) / 100);
        }
        
        respuesta = resultado.toString();
        return respuesta;
    }
    
